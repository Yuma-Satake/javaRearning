class SwitchTest {
    public static void main(String args[]) {
        int num = 1;
        
        switch (num) {
        case 1: System.out.print("case1 "); break;
        case 2: System.out.print("case2 ");
        case 3: System.out.print("case3 "); break;
        default: System.out.print("default ");
        }
    }
}