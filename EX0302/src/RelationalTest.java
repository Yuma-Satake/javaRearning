class RelationalTest {
    public static void main(String args[]) {
        int num1 = 10;
        int num2 = 10;
        int num3 = 20;
        int num4 = 30;
        
        System.out.println(num1 != num2);
        System.out.println(num1 < num3);
        System.out.println(num1 >= num2);
        System.out.println(num1 == num4);
    }
}